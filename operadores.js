// Suma
let resultadoSuma = 10 + 5;
console.log(`La suma es: ${resultadoSuma}`);

// Resta
let resultadoResta = 20 - 7;
console.log(`La resta es: ${resultadoResta}`);

// Multiplicación
let resultadoMultiplicación = 3 * 4;
console.log(`La multiplicación es: ${resultadoMultiplicación}`);

// División
let resultadoDivision = 16 / 4;
console.log(`La división es: ${resultadoDivision}`);

// Residuo
let resultadoResiduo = 10 % 3;
console.log(`El residuo es: ${resultadoResiduo}`);

// Incremento
let numero = 1
numero ++;
console.log(`El incremento es: ${numero}`);

// Decremento
let numeroo = 5;
numeroo --;
console.log(`El decremento es: ${numeroo}`);


